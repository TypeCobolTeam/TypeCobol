    public enum TokenFamily
    {
        // 0 -> 0 : Error
        Invalid = 0,

        // 1 -> 3 : Whitespace
        // p46: The separator comma and separator semicolon can
        // be used anywhere the separator space is used.
        Whitespace = 1,

        // 4 -> 5 : Comments
        Comments = 4,

        // 6 -> 11 : Separators - Syntax
        SyntaxSeparator = 6,

        // 12 -> 16 : Special character word - Arithmetic operators
        ArithmeticOperator = 12,

        // 17 -> 21 : Special character word - Relational operators
        RelationalOperator = 17,

        // 22 -> 27 : Literals - Alphanumeric
        AlphanumericLiteral = 22,

        // 28 -> 31 : Literals - Numeric
        NumericLiteral = 28,

        // 32 -> 34 : Literals - Syntax tokens
        SyntaxLiteral = 32,

        // 35 -> 39 : Symbols
        Symbol = 35,

        // 40 -> 58 : Keywords - Compiler directive starting tokens
        CompilerDirectiveStartingKeyword = 40,

        // 59 -> 154 : Keywords - Code element starting tokens
        CodeElementStartingKeyword = 59,

        // 155 -> 187 : Keywords - Special registers
        SpecialRegisterKeyword = 155,

        // 188 -> 201 : Keywords - Figurative constants
        FigurativeConstantKeyword = 188,

        // 202 -> 203 : Keywords - Special object identifiers
        SpecialObjectIdentifierKeyword = 202,

        // 204 -> 498 : Keywords - Syntax tokens
        SyntaxKeyword = 204,

        // 499 -> 501 : Keywords - Cobol V6
        CobolV6Keyword = 499,

        // 502 -> 503 : Keywords - Cobol 2002
        Cobol2002Keyword = 502,

        // 504 -> 508 : Keywords - TypeCobol
        TypeCobolKeyword = 504,

        // 509 -> 509 : TypeCobol Operators
        TypeCobolOperators = 509,

        // 510 -> 512 : Compiler directives
        CompilerDirective = 510,

        // 513 -> 513 : Internal token groups (used by the preprocessor only)
        InternalTokenGroup = 513,

        // 514 -> 525 : Formalized Comments Tokens
        FormalizedCommentsFamily = 514,

        // 526 -> 527 : Multilines Comments Tokens
        MultilinesCommentsFamily = 526,

        // 528 -> 822 : SQL keywords
        SqlKeywords = 528,

        // 823 -> 823 : SQL Syntax separators
        SqlSyntaxSeparators = 823,

        // 824 -> 826 : SQL Literals
        SqlLiterals = 824
    }

