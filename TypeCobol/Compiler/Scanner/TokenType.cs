    public enum TokenFamily
    {
        //          0 : Error
        Invalid=0,
        //   1 ->   3 : Whitespace
        // p46: The separator comma and separator semicolon can
        // be used anywhere the separator space is used.
        Whitespace=1,
        //   4 ->   5 : Comments
        Comments=4,
        // 6 ->  11 : Separators - Syntax
        SyntaxSeparator=6,
        //  12 ->  16 : Special character word - Arithmetic operators
        ArithmeticOperator=12,
        //  17 ->  21 : Special character word - Relational operators
        RelationalOperator=17,
        //  22 ->  27 : Literals - Alphanumeric
        AlphanumericLiteral=22,
        //  28 ->  31 : Literals - Numeric 
        NumericLiteral=28,
        //  32 ->  34 : Literals - Syntax tokens
        SyntaxLiteral=32,
        //  35 ->  39 : Symbols
        Symbol=35,
        //  40 ->  58 : Keywords - Compiler directive starting tokens
        CompilerDirectiveStartingKeyword=40,
        //  59 ->  154 : Keywords - Code element starting tokens
        CodeElementStartingKeyword=59,
        // 155 -> 185 : Keywords - Special registers
        SpecialRegisterKeyword=155,

        // 187 -> 200 : Keywords - Figurative constants
        FigurativeConstantKeyword=187, 
        // 201 -> 202 : Keywords - Special object identifiers
        SpecialObjectIdentifierKeyword=201,
        // 203 -> 496 : Keywords - Syntax tokens
        SyntaxKeyword=203,
        // 497 -> 499 : Keywords - Cobol V6
        CobolV6Keyword = 497,
        // 500 -> 501 : Keywords - Cobol 2002
        Cobol2002Keyword = 500,
        // 502 -> 506 : Keywords - TypeCobol
        TypeCobolKeyword = 502,

        // 507-> 507 : Operators - TypeCobol
        TypeCobolOperators= 507, 

        // 508 -> 510 : Compiler directives
        CompilerDirective = 508,
        // 511 -> 511 : Internal token groups - used by the preprocessor only
        InternalTokenGroup = 511,
        // 512 -> 523 : Formalized Comments Tokens
        FormalizedCommentsFamily = 512,
        // 524 -> 525 : Multilines Comments Tokens
        MultilinesCommentsFamily = 524
    }

