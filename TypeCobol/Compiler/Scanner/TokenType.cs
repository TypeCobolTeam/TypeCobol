    public enum TokenFamily
    {
        // 0 -> 0 : Error
        Invalid = 0,

        // 1 -> 3 : Whitespace
        // p46: The separator comma and separator semicolon can
        // be used anywhere the separator space is used.
        Whitespace = 1,

        // 4 -> 5 : Comments
        Comments = 4,

        // 6 -> 11 : Separators - Syntax
        SyntaxSeparator = 6,

        // 12 -> 16 : Special character word - Arithmetic operators
        ArithmeticOperator = 12,

        // 17 -> 21 : Special character word - Relational operators
        RelationalOperator = 17,

        // 22 -> 29 : Literals - Alphanumeric
        AlphanumericLiteral = 22,

        // 30 -> 33 : Literals - Numeric
        NumericLiteral = 30,

        // 34 -> 36 : Literals - Syntax tokens
        SyntaxLiteral = 34,

        // 37 -> 41 : Symbols
        Symbol = 37,

        // 42 -> 71 : Keywords - Compiler directive starting tokens
        CompilerDirectiveStartingKeyword = 42,

        // 72 -> 167 : Keywords - Code element starting tokens
        CodeElementStartingKeyword = 72,

        // 168 -> 200 : Keywords - Special registers
        SpecialRegisterKeyword = 168,

        // 201 -> 214 : Keywords - Figurative constants
        FigurativeConstantKeyword = 201,

        // 215 -> 216 : Keywords - Special object identifiers
        SpecialObjectIdentifierKeyword = 215,

        // 217 -> 511 : Keywords - Syntax tokens
        SyntaxKeyword = 217,

        // 512 -> 515 : Keywords - Cobol V6
        CobolV6Keyword = 512,

        // 516 -> 517 : Keywords - Cobol 2002
        Cobol2002Keyword = 516,

        // 518 -> 522 : Keywords - TypeCobol
        TypeCobolKeyword = 518,

        // 523 -> 523 : TypeCobol Operators
        TypeCobolOperators = 523,

        // 524 -> 526 : Compiler directives
        CompilerDirective = 524,

        // 527 -> 527 : Internal token groups (used by the preprocessor only)
        InternalTokenGroup = 527,

        // 528 -> 539 : Formalized Comments Tokens
        FormalizedCommentsFamily = 528,

        // 540 -> 541 : Multilines Comments Tokens
        MultilinesCommentsFamily = 540,

        // 542 -> 836 : SQL keywords
        SqlKeywords = 542,

        // 837 -> 837 : SQL Syntax separators
        SqlSyntaxSeparators = 837,

        // 838 -> 840 : SQL Literals
        SqlLiterals = 838
    }

