    public enum TokenFamily
    {
        // 0 -> 0 : Error
        Invalid = 0,

        // 1 -> 3 : Whitespace
        // p46: The separator comma and separator semicolon can
        // be used anywhere the separator space is used.
        Whitespace = 1,

        // 4 -> 5 : Comments
        Comments = 4,

        // 6 -> 11 : Separators - Syntax
        SyntaxSeparator = 6,

        // 12 -> 16 : Special character word - Arithmetic operators
        ArithmeticOperator = 12,

        // 17 -> 21 : Special character word - Relational operators
        RelationalOperator = 17,

        // 22 -> 27 : Literals - Alphanumeric
        AlphanumericLiteral = 22,

        // 28 -> 31 : Literals - Numeric
        NumericLiteral = 28,

        // 32 -> 34 : Literals - Syntax tokens
        SyntaxLiteral = 32,

        // 35 -> 39 : Symbols
        Symbol = 35,

        // 40 -> 69 : Keywords - Compiler directive starting tokens
        CompilerDirectiveStartingKeyword = 40,

        // 70 -> 165 : Keywords - Code element starting tokens
        CodeElementStartingKeyword = 70,

        // 166 -> 198 : Keywords - Special registers
        SpecialRegisterKeyword = 166,

        // 199 -> 212 : Keywords - Figurative constants
        FigurativeConstantKeyword = 199,

        // 213 -> 214 : Keywords - Special object identifiers
        SpecialObjectIdentifierKeyword = 213,

        // 215 -> 509 : Keywords - Syntax tokens
        SyntaxKeyword = 215,

        // 510 -> 512 : Keywords - Cobol V6
        CobolV6Keyword = 510,

        // 513 -> 514 : Keywords - Cobol 2002
        Cobol2002Keyword = 513,

        // 515 -> 519 : Keywords - TypeCobol
        TypeCobolKeyword = 515,

        // 520 -> 520 : TypeCobol Operators
        TypeCobolOperators = 520,

        // 521 -> 523 : Compiler directives
        CompilerDirective = 521,

        // 524 -> 524 : Internal token groups (used by the preprocessor only)
        InternalTokenGroup = 524,

        // 525 -> 536 : Formalized Comments Tokens
        FormalizedCommentsFamily = 525,

        // 537 -> 538 : Multilines Comments Tokens
        MultilinesCommentsFamily = 537,

        // 539 -> 833 : SQL keywords
        SqlKeywords = 539,

        // 834 -> 834 : SQL Syntax separators
        SqlSyntaxSeparators = 834,

        // 835 -> 837 : SQL Literals
        SqlLiterals = 835
    }

